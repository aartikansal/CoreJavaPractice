
/*
Three methods and a maximum

*/

public class Max_overload {
    public static void main(String[] args) {

    }
    public static  int max(int a,int b){
        if(a>b){
            return a;
        }
        else {
            return b;
        }
    }
    public static long max(long z, long u){
        if(z>u){
            return z;
        }
        else {
            return u;
        }
    }
    public static double max(double x, double y){
        if(x>y)
        {
            return x;
        }
        else {
            return y;
        }
    }
}
