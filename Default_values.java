public class Default_values {
    int intVar;
    double doubleVar;
    Double DoubleVar;
    boolean booleanVar;
    Object ObjectVar;
    Exception ExceptionVar;
    String StringVar;

    public static void main(String[] args) {
        Default_values default_values = new Default_values();
        System.out.println(default_values.intVar);
        System.out.println(default_values.doubleVar);
        System.out.println(default_values.DoubleVar);
        System.out.println(default_values.booleanVar);
        System.out.println(default_values.ObjectVar);
        System.out.println(default_values.ExceptionVar);
        System.out.println(default_values.StringVar);

    }
}